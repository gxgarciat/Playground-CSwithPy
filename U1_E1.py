def main():

    """
    Convert the following into code that uses a while loop.

    prints 2
    prints 4
    prints 6
    prints 8
    prints 10
    prints Goodbye!

    :return:
    """
    i = 0
    while i < 10:
        i = i + 2
        print(i)
    print('Goodbye!')

if __name__ == "__main__":
    main()