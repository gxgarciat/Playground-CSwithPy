def main():

    """
    Convert the following into code that uses a while loop.

    prints Hello!
    prints 10
    prints 8
    prints 6
    prints 4
    prints 2

    :return:
    """
    print('Hello!')

    i = 10
    while i > 0:
        print(i)
        i = i - 2

if __name__ == "__main__":
    main()